                <h2>Регалии (символы) президента Российской Федерации</h2>
                <hr>
                <div class="photol"><img src="1024px-Standard_of_the_President_of_the_Russian_Federation.svg.png" alt=""
                        width="250px">
                    <figcaption>Штандарт президента Российской Федерации</figcaption>
                </div>

                <p>
                    Статьёй 7 Закона РСФСР от 27 июня 1991 года «О вступлении в должность Президента РСФСР»
                    определялось, что президент РСФСР имеет круглую печать с изображением герба РСФСР и надписью
                    «Президент
                    Российской Советской Федеративной Социалистической Республики»; в ст. 9 того же закона указывалось,
                    что
                    в месте официального пребывания президента РСФСР и вице-президента РСФСР поднимается Государственный
                    флаг Российской Советской Федеративной Социалистической Республики.
                </p>
                <div class="photo"><img src="Russia_president_emblem.jpg" width="250px">
                    <figcaption>Знак президента Российской Федерации</figcaption>
                </div>
                <p>
                    После отмены данного закона указами президента Российской Федерации № 319 от 15 февраля 1994 года «О
                    штандарте (флаге) Президента Российской Федерации», № 1138 от 5 августа 1996 года «Об официальных
                    символах президентской власти и их использовании при вступлении в должность вновь избранного
                    Президента
                    Российской Федерации», и № 906 от 27 июля 1999 года «Об утверждении описания символа президентской
                    власти — Знака Президента Российской Федерации» символами президента были утверждены президентский
                    штандарт, президентский знак и специальный экземпляр Конституции России.
                </p>
                <br>
                <br>
                <br>
                <div class="photol"><img src="Red_copy_of_the_Russian_constitution.jpg" alt="" width="250px">
                    <figcaption>Специальный экземпляр Конституции Российской Федерации</figcaption>
                </div>
                <p>
                    После указа и. о. президента Российской Федерации № 832 от 6 мая 2000 года «О внесении изменений и
                    дополнения в некоторые указы президента Российской Федерации» специальный экземпляр Конституции
                    утратил
                    официальный статус символа президента России.
                </p>

