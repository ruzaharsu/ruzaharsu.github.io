            <h2>Срок полномочий</h2>
            <hr>
            <p>
                Президент РСФСР, избранный в соответствии с Конституцией РСФСР 1978 года на 5 лет, осуществлял свои
                полномочия в 1991—1993 годах.
            </p>
            <p>
                С принятием Конституции РФ 1993 года срок полномочий был сокращён до 4 лет, что практиковалось в
                1993—2008 годах.
            </p>
            <p>
                Срок 6 лет, установленный в 2008 году, стал применяеться в отношении президентов, избранных после
                принятия соответствующих поправок (то есть начиная с выборов 2012 года).
            </p>
            <p>
                Президент имел право исполнять должность не более двух сроков подряд в 1991—2020 годах; с 2020 года — не
                более двух сроков.
            </p>
            <h2>Прававой статус</h2>
            <hr>
            <p>
                Президент Российской Федерации — единственная должность, на которую избирается один из кандидатов общим
                голосованием граждан Российской Федерации.
            </p>
            <p>
                Правовой статус президента Российской Федерации закреплён в статьях 11, 78, 80-93, 107-109, 111-117,
                125, 128, 134 Конституции Российской Федерации.
            </p>
            <p>
                Наряду с полномочиями главы государства президент Российской Федерации имеет право председательствовать
                на заседаниях Правительства Российской Федерации. Президент Российской Федерации может давать поручения
                правительству, он также осуществляет руководство отдельными федеральными органами исполнительной власти.
            </p>
            <p>
                В отличие от конституции Пятой французской республики, где политику государства определяет и проводит
                правительство, а президент почти всегда председательствует на его заседаниях, президент Российской
                Федерации самостоятельно и в полной ответственности «определяет основные направления внутренней и
                внешней политики государства» (статья 80 Конституции Российской Федерации) и «руководит внешней
                политикой» (статья 86 Конституции Российской Федерации). Правительство же «осуществляет исполнительную
                власть» (статья 110 Конституции РФ), то есть, определяет оперативные направления внутренней политики и
                проводит политику, определённую президентом в его указах.
            </p>
            <p>
                Указы и распоряжения президента, как и постановления и распоряжения правительства, обязательны для
                исполнения в Российской Федерации и являются подзаконными актами. Однако президент может отменять
                постановления и распоряжения Правительства Российской Федерации.
            </p>
            <p>
                Формально, правительство — «коллективный» глава исполнительной власти. Однако, определяя своими указами
                направления деятельности правительства, других органов исполнительной власти, президент является
                «фактическим» главой исполнительной власти (это не управление текущей работой, которая характеризуется
                как аппаратно-управленческая деятельность).
            </p>
            <p>
                Ввиду такой двухступенчатости исполнительной власти термин «глава исполнительной власти» в Конституции
                Российской Федерации (как и Франции) отсутствует (председатель правительства не является таковым, так
                как властными полномочиями обладают президент и правительство):
            </p>
            <p>
                "На основании и во исполнение Конституции Российской Федерации, законов, нормативных указов президента
                Правительство Российской Федерации издаёт постановления и распоряжения, обеспечивает их исполнение. Если
                акты правительства противоречат Конституции, федеральным законам Российской Федерации или указам
                президента, то они могут быть отменены президентом. " (статья 115 Конституции РФ). Но согласно статье 90
                указы президента не могут противоречить федеральным законам, которые в свою очередь, согласно 109
                статье, после отклонения президентом, при вторичном рассмотрении Государственной Думой и Советом
                Федерации и повторном принятии его более двумя третями голосов, подлежит подписанию им в течение 1
                недели.
            </p>
            <p>
                «Председатель правительства Российской Федерации в соответствии с Конституцией Российской Федерации,
                федеральными законами и указами президента Российской Федерации определяет основные направления
                деятельности правительства Российской Федерации и организует его работу» (статья 113 Конституции РФ).
            </p>
            <p>
                «Президент Российской Федерации может принять решение об отставке правительства Российской Федерации»
                (статья 117 Конституции РФ).
            </p>
            <p>
                Таким образом, выборы президента Российской Федерации с его программой можно назвать «выборами
                исполнительной власти» федерального уровня.
            </p>
            <p>
                По отношению к законодательной власти президент, как и правительство, имеет право законодательной
                инициативы. Однако правительство вносит в Думу законопроекты, касающиеся его компетенции (в основном
                бюджет), а президент вносит законопроекты, касающиеся всех сторон общественной жизни. Он обращается к
                Федеральному собранию с ежегодными посланиями о положении в стране, об основных направлениях политики
                государства (статья 84 Конституции РФ), в том числе и законодательной политики (рекомендует принятие
                определённых законов).
            </p>
            <p>
                Президент РФ представляет Совету Федерации кандидатуры для назначения судей Конституционного и
                Верховного судов, а также кандидатуру Генерального прокурора, вносит предложение о его освобождении,
                назначает судей других федеральных судов, назначает и освобождает высшее командование Вооружённых сил,
                назначает и отзывает послов РФ, принимает верительные и отзывные грамоты иностранных послов.
            </p>
            <p>
                Президент Российской Федерации обладает неприкосновенностью (статья 91 Конституции РФ), отличной от
                понятия «личная неприкосновенность», предусмотренной для всех граждан Российской Федерации статьёй 22
                Конституции, но не конкретизируемой законодательством Российской Федерации. Считается, что
                неприкосновенность означает невозможность привлечения президента к уголовной или административной
                ответственности или применения к нему каких-либо принудительных мер (допрос и т. п.).
            </p>
            <p>
                Статья 93 Конституции устанавливает порядок отрешения президента от должности «на основании выдвинутого
                Государственной думой обвинения в государственной измене или совершении иного особо тяжкого преступления
                <…>», который предусматривает участие двух ветвей государственной власти: законодательной и судебной.
            </p>
            <p>
                Согласно статье 7 Федерального закона «О государственной охране», в отличие от прочих лиц,
                замещающих государственные должности, которым предоставляется государственная охрана, «президент
                Российской Федерации в течение срока своих полномочий не вправе отказаться от государственной охраны».
                Государственной охране также подлежат члены семьи президента, а после окончания срока полномочий
                президента государственная охрана предоставляется ему пожизненно.
            </p>
            <h2>Порядок вступления в должность</h2>
            <hr>
            <p>
                Первый президент России был избран в 1991 году сроком на 5 лет. В Конституции РФ 1993 года срок
                полномочий президента был сокращён до 4 лет. Однако согласно п.3 Заключительных и переходных положений
                Конституции президент Ельцин осуществлял полномочия полный срок, на который он был избран (то есть до
                1996 года). В соответствии с изменением Конституции, вступившим в силу 31 декабря 2008 года, начиная с
                выборов 2012 года, президент Российской Федерации избирается на шестилетний срок.

            </p>
            <p>
                При вступлении в должность президент Российской Федерации приносит народу следующую присягу (статья 82
                Конституции РФ):
            </p>
            <blockquote>
                <i> «Клянусь при осуществлении полномочий Президента Российской Федерации уважать и охранять права и
                    свободы
                    человека и гражданина, соблюдать и защищать Конституцию Российской Федерации, защищать суверенитет и
                    независимость, безопасность и целостность государства, верно служить народу».
                </i>
            </blockquote>
            <p>
                Присяга приносится в торжественной обстановке в присутствии членов Совета Федерации, депутатов
                Государственной думы и судей Конституционного суда Российской Федерации.
            </p>
