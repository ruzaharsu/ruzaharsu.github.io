                        стране, охраны суверенитета Российской Федерации, ее независимости и государственной
                        целостности,
                        предотвращения внутренних и внешних угроз формирует и возглавляет Совет безопасности Российской
                        Федерации, статус которого определяется федеральным законом;
                    </li>
                    <li>
                        утверждает военную доктрину Российской Федерации;
                    </li>
                    <li>
                        формирует администрацию президента Российской Федерации в целях обеспечения реализации своих
                        полномочий;
                    </li>
                    <div class="photo">
                        <img src="Russian_president_telephones.jpg" alt="">
                        <figcaption>Рабочие телефоны в кабинете президента</figcaption>
                    </div>
                    <li>
                        назначает и освобождает полномочных представителей президента Российской Федерации;
                    </li>
                    <li>
                        назначает и освобождает высшее командование Вооружённых Сил Российской Федерации;
                    </li>
                    <li>
                        назначает и отзывает дипломатических представителей Российской Федерации в иностранных
                        государствах
                        и международных организациях. В соответствии с требованием Конституции эти назначения могут быть
                        произведены президентом после консультаций с соответствующими комитетами или комиссиями палат
                        Федерального собрания;
                    </li>
                    <li>
                        назначает выборы Государственной думы в соответствии с Конституцией и федеральным законом;
                    </li>
                    <li>
                        распускает Государственную думу в случаях и порядке, предусмотренных Конституцией;
                    </li>
                    <li>
                        назначает референдум в порядке, установленном федеральным конституционным законом;
                    </li>
                    <li>
                        вносит законопроекты в Государственную думу;
                    </li>
                    <li>
                        подписывает и обнародует федеральные законы в течение четырнадцати дней с момента получения.
                        Если
                        президент в течение четырнадцати дней с момента поступления федерального закона отклонит его, то
                        Государственная дума и Совет федерации в установленном Конституцией порядке вновь рассматривают
                        данный закон. Если при повторном рассмотрении федеральный закон будет одобрен в ранее принятой
                        редакции большинством не менее двух третей голосов от общего числа членов Совета Федерации и
                        депутатов Государственной думы, он подлежит подписанию президентом РФ в течение семи дней и
                        обнародованию;
                    </li>
                    <li>
                        обращается к Федеральному собранию с ежегодными посланиями о положении в стране, об основных
                        направлениях внутренней и внешней политики государства;
                    </li>
                    <li>
                        может использовать согласительные процедуры для разрешения разногласий между органами
                        государственной власти Российской Федерации и органами государственной власти субъектов
                        Российской
                        Федерации, а также между органами государственной власти субъектов Российской Федерации. В
                        случае
                        недостижения согласованного решения он может передать разрешение спора на рассмотрение
                        соответствующего суда;
                    </li>
                    <li>
                        вправе приостанавливать действие актов органов исполнительной власти субъектов Российской
                        Федерации;
                    </li>
                    <li>
                        издаёт указы и распоряжения;
                    </li>
                    <li>
                        может отменять постановления и распоряжения Правительства Российской Федерации;
                    </li>
                    <li>
                        осуществляет руководство внешней политикой Российской Федерации;
                    </li>
                    <li>
                        ведёт переговоры и подписывает международные договоры Российской Федерации;
                    </li>
                    <li>
                        подписывает ратификационные грамоты;
                    </li>
                    <li>
                        принимает верительные и отзывные грамоты аккредитуемых при нём дипломатических представителей;
                    </li>
                    <li>
                        в случае агрессии против Российской Федерации или непосредственной угрозы агрессии президент
                        Российской Федерации вводит на территории Российской Федерации или в отдельных её местностях
                        военное
                        положение с незамедлительным сообщением об этом Совету Федерации и Государственной думе;
                    </li>
                    <li>
                        решает вопросы гражданства Российской Федерации и предоставления политического убежища;
                    </li>
                    <li>
                        награждает государственными наградами Российской Федерации, присваивает почётные звания
                        Российской
                        Федерации, высшие воинские и высшие специальные звания;
                    </li>
                    <li>
                        осуществляет помилование.
                    </li>
                </ul>
                <p>
                    В процессе осуществления своих полномочий президент Российской Федерации издаёт указы и
                    распоряжения, являющиеся обязательными для исполнения на всей территории Российской Федерации. Указы
                    и
                    распоряжения президента Российской Федерации не должны противоречить Конституции Российской
                    Федерации и
                    федеральным законам.
                </p>
