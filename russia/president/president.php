            <h1>Президент Российской Федерации</h1>
            <hr>
            <p>
                <b> Президент Российской Федерации </b> — высшая государственная должность Российской Федерации, а также
                лицо,
                избранное на эту должность.
            </p>
            <p>
                Президент Российской Федерации является главой государства. Многие полномочия президента либо имеют
                непосредственно исполнительный характер, либо приближены к исполнительной власти. Наряду с этим, по
                мнению некоторых исследователей, президент не относится к какой-либо одной ветви власти, а возвышается
                над ними, поскольку осуществляет координирующие функции и имеет право роспуска Государственной думы
            </p>

            <p>
                Президент Российской Федерации является также гарантом Конституции Российской Федерации, прав и свобод
                человека и гражданина и верховным главнокомандующим Вооружёнными силами Российской Федерации.
            </p>
            <p>
                В соответствии с Конституцией Российской Федерации и федеральными законами президент Российской
                Федерации определяет основные направления внутренней и внешней политики.
            </p>
            <p>
                С 7 мая 2012 года президентом Российской Федерации является Владимир Владимирович Путин.

